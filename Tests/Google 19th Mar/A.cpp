int solve(int n, string s) {

    vector<vector<int>> freq(2, vector<int>(n + 1));
    for (int i = 1; i <= n; i++) {
        freq[0][i] += s[i - 1] == '0';
        freq[1][i] += s[i - 1] == '1';
    }

    int ans = 0;
    for (int i = 1; i <= n; i++) {
        for (int j = i; j <= n; j++) {
            int zeros = freq[0][j] - freq[0][i - 1];
            int ones = freq[1][j] - freq[1][i - 1];
            int tot = j - i + 1;
            ans = max(ans, tot % 2 == 0 && tot >= zeros * 2 && tot >= ones * 2 ? tot : 0);
        }
    }

    return ans;
}

